// LoginPage.jsx

import React, { useState } from 'react';

const LoginPage = () => {
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [error, setError] = useState('');

  const handleSubmit = async (e) => {
    e.preventDefault();
    setError('');

    // Add your login logic here, such as calling an API to authenticate the user
    try {
      // Replace this with your actual login logic
      const response = await fetch('https://nest-socket-server.onrender.com/user/login', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({ email, password }),
      });

      if (!response.ok) {
        throw new Error('Login failed');
      }

      // If login is successful, call the onLogin callback with user data
      const access_token  = await response.json();

      // Store the JWT token in localStorage
      localStorage.setItem('access_token', JSON.stringify(access_token));
    } catch (error) {
      setError('Login failed. Please check your email and password.');
      console.log(error, error?.message)
    }
  };

  return (
    <div>
      <h1>Login</h1>
      {error && <div className="error">{error}</div>}
      <form onSubmit={handleSubmit}>
        <div>
          <label>Email:</label>
          <input type="email" value={email} onChange={(e) => setEmail(e.target.value)} required />
        </div>
        <div>
          <label>Password:</label>
          <input type="password" value={password} onChange={(e) => setPassword(e.target.value)} required />
        </div>
        <button type="submit">Login</button>
      </form>
    </div>
  );
};

export default LoginPage;
